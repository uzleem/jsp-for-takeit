-- 주문
CREATE TABLE ORDERS (
	ORDER_NO VARCHAR2(20) NOT NULL -- 주문번호,
  ,	RECEIVE_METHOD VARCHAR2(20) NOT NULL -- 수령방법,
  ,	RECIPIENT_NAME VARCHAR2(50) NOT NULL -- 수령인,
  ,	RECIPIENT_POSTNO CHAR(5) NULL -- 수령인우편번호,
  ,	RECIPIENT_ADDR VARCHAR2(500) NULL -- 수령인 상세주소,
  ,	RECIPIENT_MOBILE CHAR(13) NOT NULL -- 수령인 연락처,
  ,	SHIP_REQUEST VARCHAR2(100) NULL -- 배송요청사항,
  ,	SHIP_STATUS_CODE VARCHAR2(10) NULL -- 배송상태코드,
