-- 상품
CREATE TABLE ITEM (
	ITEM_NO VARCHAR2(20) NOT NULL -- 상품번호,
  ,	SELLER_ID varchar2(50) NOT NULL -- 판매자회원아이디,
  ,	ITEM_NAME VARCHAR2(50) NOT NULL -- 상품명,
  ,	ITEM_PRICE NUMBER(8) NOT NULL -- 판매가,
  ,	SALES_UNIT VARCHAR2(20) NULL -- 판매단위,
  ,	ITEM_ORIGIN VARCHAR2(20) NOT NULL -- 원산지,
  ,	ITEM_STOCK NUMBER(3,0) NOT NULL -- 재고량,
  ,	ITEM_IMG VARCHAR2(200) NOT NULL -- 상품이미지,
